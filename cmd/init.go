func initRootDir(path string) error {
	if _, err := os.Stat(path); errors.Is(err, os.ErrNotExist) {
		err := os.Mkdir(path, os.ModePerm)
		if err != nil {
			return err
		}
	}

	return nil
}

func initialize(cmd *cobra.Command, args []string) {
	initRootDir(args[0])
}
